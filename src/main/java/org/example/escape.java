package org.example;

public class escape {

        public static void main(String[] args) {
            System.out.println("Hello\nWorld");         // newline
            System.out.println("Hello\tWorld");         // tab
            System.out.println("She said \"Hi\"");      // double quotes
            System.out.println("Path: C:\\Users\\Java"); // backslash
            System.out.println("Backspace demo: ABC\bD"); // removes C
            System.out.println("Unicode A: \u0041");// prints A
            System.out.println("hello \n hi");
            System.out.println("123344\b5");
        }
    }


